type SupportedLanguage = 'en' | 'zh';
const LANGUAGE_SETTINGS_KEY = 'language';

function normalizeSupportedLanguage(value: unknown): SupportedLanguage | null {
  const v = String(value || '').trim().toLowerCase();
  if (!v) return null;
  if (v.startsWith('zh')) return 'zh';
  if (v.startsWith('en')) return 'en';
  return null;
}

function detectSystemLanguage(): SupportedLanguage {
  if (typeof navigator === 'undefined') {
    return 'en';
  }
  const candidates = [
    ...(Array.isArray(navigator.languages) ? navigator.languages : []),
    navigator.language,
  ];
  for (const c of candidates) {
    const normalized = normalizeSupportedLanguage(c);
    if (normalized) return normalized;
  }
  return 'en';
}

async function getPersistedLanguageFromSettings(): Promise<SupportedLanguage | null> {
  try {
    const settings = (await invoke('get_settings')) as Record<string, unknown>;
    return normalizeSupportedLanguage(settings?.[LANGUAGE_SETTINGS_KEY]);
  } catch {
    return null;
  }
}

