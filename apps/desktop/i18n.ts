let i18nInitPromise: Promise<void> | null = null;

export async function initializeI18n(): Promise<void> {
  if (i18n.isInitialized) return;
  if (i18nInitPromise) return i18nInitPromise;

  i18nInitPromise = (async () => {
    const persisted = await getPersistedLanguageFromSettings();
    const initialLang = persisted || detectSystemLanguage();

    await i18n.use(initReactI18next).init({
      resources,
      lng: initialLang,
      fallbackLng: 'en',
      interpolation: {
        escapeValue: false,
      },
    });

    try {
      localStorage.removeItem('i18nextLng');
    } catch {
      // ignore
    }
  })();

  return i18nInitPromise;
}

void initializeI18n();

export default i18n;
